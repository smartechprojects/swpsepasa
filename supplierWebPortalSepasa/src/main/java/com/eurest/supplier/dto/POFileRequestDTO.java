package com.eurest.supplier.dto;

public class POFileRequestDTO {
	
	private String nit_proveedor;
	private String empresa_oc;
	private String tipo_oc;
	private String numero_oc;
	
	public String getNit_proveedor() {
		return nit_proveedor;
	}
	public void setNit_proveedor(String nit_proveedor) {
		this.nit_proveedor = nit_proveedor;
	}
	public String getEmpresa_oc() {
		return empresa_oc;
	}
	public void setEmpresa_oc(String empresa_oc) {
		this.empresa_oc = empresa_oc;
	}
	public String getTipo_oc() {
		return tipo_oc;
	}
	public void setTipo_oc(String tipo_oc) {
		this.tipo_oc = tipo_oc;
	}
	public String getNumero_oc() {
		return numero_oc;
	}
	public void setNumero_oc(String numero_oc) {
		this.numero_oc = numero_oc;
	}
	
}
